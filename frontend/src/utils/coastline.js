/**
 * Draw US coastline and Great Lakes on canvas.
 * @param {CanvasRenderingContext2D} ctx
 * @param {object} viewport - {minLat, maxLat, minLon, maxLon, width, height}
 * @param {function} toPixel - latLonToPixel function
 */
export function drawCoastline(ctx, viewport, toPixel) {
  // Draw land mass
  ctx.fillStyle = '#131e30';
  ctx.strokeStyle = '#1e3048';
  ctx.lineWidth = 1;

  for (const polygon of US_COASTLINE) {
    ctx.beginPath();
    let started = false;
    for (const [lat, lon] of polygon) {
      const [x, y] = toPixel(lat, lon, viewport);
      if (!started) {
        ctx.moveTo(x, y);
        started = true;
      } else {
        ctx.lineTo(x, y);
      }
    }
    ctx.closePath();
    ctx.fill();
    ctx.stroke();
  }

  // Draw Great Lakes as "water holes" in the land
  ctx.fillStyle = '#0a1628'; // same as ocean
  ctx.strokeStyle = '#1e3048';
  ctx.lineWidth = 0.8;

  for (const lake of US_LAKES) {
    ctx.beginPath();
    let started = false;
    for (const [lat, lon] of lake) {
      const [x, y] = toPixel(lat, lon, viewport);
      if (!started) {
        ctx.moveTo(x, y);
        started = true;
      } else {
        ctx.lineTo(x, y);
      }
    }
    ctx.closePath();
    ctx.fill();
    ctx.stroke();
  }
}
